function agregado() {
    $('#alerta').addClass('visible');
    $('#alerta').removeClass('invisible');
    $('#texto').html('Agregado con exito');
}

function eliminado() {
    $('#alerta').addClass('visible');
    $('#alerta').removeClass('invisible');
    $('#texto').html('Eliminado con exito');
}

function modificado() {
    $('#alerta').addClass('visible');
    $('#alerta').removeClass('invisible');
    $('#texto').html('Modificado con exito');
}
